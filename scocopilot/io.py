### IO operations

def fileio(filename=None, option="read"):

    if option in ("w", "write"):

        try:
            return True if os.path.getsize(filename) == 0 else False

        except OSError:
            return True

    elif option in ("r","s","read","silent"):

        try:
            if os.path.getsize(filename) == 0:
                if option in ("r","read"):
                    stdout(code="error", source=str(filename), mssg="is empty")
                return False
            else:
                return True

        except OSError:
            if option in ("r","read"):
                stdout(code="error", source=str(filename), mssg="not found")
            return False
    
